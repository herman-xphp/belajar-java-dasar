public class TipeDataNumber {
    public static void main(String[] args) {
        // bulat
        byte iniByte = 100;
        short iniShort = 10000;
        int integer = 1000000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000000000000L;

        // pecahan
        float iniFloat = 10.10F;
        double iniDouble = 10.10;

        // literal
        int decimalInt = 25;
        int hexDecimal = 0xFFFFF;
        int binaryDecimal = 0b1010101;

        // underscore
        long balance = 1_000_000_000L;
        int sum = 60_000_000;

    }
}
